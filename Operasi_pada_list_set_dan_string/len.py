# List

contoh_list = [1, 3, 3, 5, 5, 5, 7, 7, 9]

print(contoh_list)  # Output: [1, 3, 3, 5, 5, 5, 7, 7, 9]
print(len(contoh_list)) # Output: 9


# Set

contoh_set = set([1, 3, 3, 5, 5, 5, 7, 7, 9])

print(contoh_set) # Output: {1, 3, 5, 7, 9}
print(len(contoh_set)) # Output: 5


# String
contoh_list = "Belajar Python"

print(contoh_list) # Output: Belajar Python
print(len(contoh_list)) # Output: 14