# konversi integer ke float
print(float(5)) # Output: 5.0

# konversi float ke integer
print(int(10.6)) # Output: 10
print(int(-10.6)) # Output: -10

# konversi dari-dan-ke string
print(int("25")) # Output: 25
print(str(25)) # Output: '25'
print(float("25")) # Output: 25.0
print(str(25.6)) # Output: '25.6'

# konversi kumpulan data
print(set([1,2,3])) # Output: {1, 2, 3}
print(tuple({5,6,7})) # Output: (5, 6, 7)
print(list('hello')) # Output: ['h', 'e', 'l', 'l', 'o']

# konversi ke dictionary
print(dict([[1,2],[3,4]])) # Output: {1: 2, 3: 4}
print(dict([(3,26),(4,44)])) # Output: {3: 26, 4: 44}